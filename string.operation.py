message = "Today is 15-Feb-2025"
upper_message = message.upper() # Convert all characters to uppercase
print(upper_message) # TODAY IS 15-FEB-2025

print(message.upper()) # TODAY IS 15-FEB-2025
print(message.lower()) # today is 15-feb-2025
print(message.title()) # Today Is 15-Feb-2025

