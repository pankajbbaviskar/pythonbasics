fruits = ['apple', 'banana', 'peach', 'grape', 'orange']
print(f"List of fruits: {fruits}")

fruits.append('mango')
print(f"List of fruits: {fruits}")

fruits.insert(1, 'kiwi')
print(f"List of fruits: {fruits}")

fruits.pop()
print(f"List of fruits: {fruits}")

fruits.sort()
print(f"List of fruits: {fruits}")
print(f"List of fruits: {fruits[::-1]}")  
